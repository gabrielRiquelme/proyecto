def sumar(op1,op2):
    '''Funcion de suma'''
    print(f'El resultado de tu suma es: {op1+op2}')

def restar(op1,op2):
    '''Funcion de resta'''
    print(f'El resultado de tu resta es: {op1-op2}')

def dividir(op1,op2):
    '''Funcion de dividir'''
    print(f'El resultado de tu division es: {op1/op2}')

def multiplicar(op1,op2):
    '''Funcion de multiplicar'''
    print(f'El resultado de tu division es: {op1*op2}')
